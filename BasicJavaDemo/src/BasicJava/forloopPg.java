package BasicJava;

public class forloopPg {
	
	    public static void main(String[] args)
	    {
	    	

	    	

	    	    
	    	        int count = 1; // Initialize a counter to keep track of even numbers
	    	        int number = 2; // Start with the first even number

	    	        System.out.println("The first 10 even numbers are:");

	    	        while (count <= 10) {
	    	            System.out.println(number);
	    	            number += 2; // Move to the next even number
	    	            count++; // Increment the counter
	    	        }
	    	    }
	    	

	        }
	    
	

	


